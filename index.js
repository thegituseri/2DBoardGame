class ShooterEnemy{
    constructor(position, circle){
        this.position = position;
        this.hitbox = {
            x: this.position.x,
            y: this.position.y,
        }
        this.circle = circle;
        this.width = 25;
        this.height = 30;
        this.radius = 4;
        this.speed = 5;
        this.elapsedFrames = 0;
        this.animationNumber = position.def;
        this.default = position.def;
        this.currentFrame = 0;
        this.animations = {
            1: {
                frameRate: 5,
                frameDelay: 7,
                imgSrc: "archerRight/idle.png"
            },
            2: {
                frameRate: 9,
                frameDelay: 3,
                imgSrc: "archerRight/attack.png"
            },
            3: {
                frameRate: 5,
                frameDelay: 7,
                imgSrc: "archerLeft/idle.png"
            },
            4: {
                frameRate: 9,
                frameDelay: 3,
                imgSrc: "archerLeft/attack.png"
            }
        }
        for(let key in this.animations){
            const img = new Image();
            img.src = this.animations[key].imgSrc;
            this.animations[key].image = img;
            this.animations[key].width = img.width / this.animations[key].frameRate;
            this.hitbox.width = img.width / this.animations[key].frameRate;
            this.hitbox.height = img.height;
            this.animations[key].height = img.height;
        }
        this.loaded = false;
        this.animations[2].image.onload = () => {
            this.loaded = true;
        }
    }
    updateFrames(){
        this.elapsedFrames++;
        if(this.elapsedFrames > 999) this.elapsedFrames = 0;
        if(this.elapsedFrames % this.animations[this.animationNumber].frameDelay == 0){
            if(this.currentFrame < this.animations[this.animationNumber].frameRate - 1){
                this.currentFrame++;
            }
            else{
                this.currentFrame = 0;
                this.animationNumber = this.default;
            }
        }

    }
    drawAndUpdate(){
        if(!this.loaded) return;
        ctx.beginPath();
        ctx.arc(this.circle.x, this.circle.y, this.radius, 0, Math.PI * 2);
        ctx.fillStyle = 'blue';
        ctx.fill();
        ctx.closePath();
        ctx.strokeRect(this.position.x, this.position.y, this.width, this.height);
        
        ctx.drawImage(this.animations[this.animationNumber].image,
            this.animations[this.animationNumber].width * this.currentFrame,14,
            this.animations[this.animationNumber].width, this.animations[this.animationNumber].height,
            this.position.x - 20, this.position.y - 6, 
            this.animations[this.animationNumber].width * 2, this.animations[this.animationNumber].height * 2
        );
        if(this.circle.shootCondition){
            this.circle.x += this.circle.xSpeed;
            this.circle.y += this.circle.ySpeed;
            if(Math.abs(this.circle.initial - this.circle.x) >= Math.abs(this.circle.len)){
                this.animationNumber = this.default + 1;
                this.currentFrame = 0;
                this.elapsedFrames = 1;
                this.circle.x = this.circle.initial + this.width/2;
                this.circle.y = this.circle.yinit + this.height/2;
            }
            //check collision
            let testX = this.circle.x;
            let testY = this.circle.y;

            if (this.circle.x < player.hitbox.x) testX = player.hitbox.x;      // test left edge
            else if (this.circle.x > player.hitbox.x+player.hitbox.width) testX = player.hitbox.x+player.hitbox.width;   // right edge
            if (this.circle.y < player.hitbox.y) testY = player.hitbox.y;      // top edge
            else if (this.circle.y > player.hitbox.y+player.hitbox.height) testY = player.hitbox.y+player.hitbox.height;   // bottom edge

            // get initial from closest edges
            let distX = this.circle.x-testX;
            let distY = this.circle.y-testY;
            let distance = Math.sqrt( (distX*distX) + (distY*distY) );

            // if the distance is less than the radius, collision!
            if (distance <= this.radius) {
                gameEnder = true;
            }

        }
    }
}

