function endGame() {
    canvas.removeEventListener('click', handleButtonClick); // Remove listener to prevent multiple clicks
    const canvasWidth = canvas.width;
    const canvasHeight = canvas.height;

    // Button dimensions
    const buttonWidth = 200;
    const buttonHeight = 50;

    // Button positions (centered on the canvas)
    const startNewGameButtonX = (canvasWidth - buttonWidth) / 2;
    const startNewGameButtonY = (canvasHeight / 2) - buttonHeight - 10;
    const restartLevelButtonX = startNewGameButtonX;
    const restartLevelButtonY = startNewGameButtonY + buttonHeight + 20;

    // Clear the canvas
    //ctx.clearRect(0, 0, canvasWidth, canvasHeight);

    // Draw 'Start a New Game' button
    ctx.fillStyle = '#000'; // Button color
    ctx.fillRect(startNewGameButtonX, startNewGameButtonY, buttonWidth, buttonHeight);
    ctx.fillStyle = '#fff'; // Text color
    ctx.font = '20px Arial';
    ctx.textAlign = 'center';
    ctx.fillText('Start a New Game', startNewGameButtonX + buttonWidth / 2, startNewGameButtonY + buttonHeight / 1.5);

    // Draw 'Restart Level' button
    ctx.fillStyle = '#000'; // Button color
    ctx.fillRect(restartLevelButtonX, restartLevelButtonY, buttonWidth, buttonHeight);
    ctx.fillStyle = '#fff'; // Text color
    ctx.fillText('Restart Level', restartLevelButtonX + buttonWidth / 2, restartLevelButtonY + buttonHeight / 1.5);

    // Add click event listener for buttons
    function handleButtonClick(event) {

        const rect = canvas.getBoundingClientRect(); // Get the canvas bounding rectangle
        const mouseX = event.clientX - rect.left; // Adjust for canvas offset
        const mouseY = event.clientY - rect.top;  // Adjust for canvas offset
        // Check if 'Start a New Game' button is clicked
        if (mouseX >= startNewGameButtonX && mouseX <= startNewGameButtonX + buttonWidth &&
            mouseY >= startNewGameButtonY && mouseY <= startNewGameButtonY + buttonHeight) {
            canvas.removeEventListener('click', handleButtonClick); // Remove listener to prevent multiple clicks,
            levelcount = 1;
            gameEnder = false;
            restartLevel();
        }

        // Check if 'Restart Level' button is clicked
        if (mouseX >= restartLevelButtonX && mouseX <= restartLevelButtonX + buttonWidth &&
            mouseY >= restartLevelButtonY && mouseY <= restartLevelButtonY + buttonHeight) {
            canvas.removeEventListener('click', handleButtonClick); // Remove listener to prevent multiple clicks
            gameEnder = false;
            restartLevel();
        }
    }

    canvas.addEventListener('click', handleButtonClick);
}

